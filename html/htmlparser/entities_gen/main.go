package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"log"
	"net/http"
	"os"
	"strconv"
	"strings"
)

var pkg = flag.String("pkg", "htmlparser", "Package name to use")
var url = flag.String("url", "https://html.spec.whatwg.org/entities.json", "The HTTP URL to download entities.json from")
var outFile = flag.String("out", "entities.go", "Output file")

func main() {
	flag.Parse()

	log.SetPrefix("[html/parser/entities_gen] ")
	log.Printf("Downloading from %s", *url)
	res, err := http.Get(*url)
	if err != nil {
		log.Fatal(err)
	}
	log.Println("Decoding JSON")
	dec := json.NewDecoder(res.Body)
	var entries map[string]map[string]any
	if err := dec.Decode(&entries); err != nil {
		log.Fatal(err)
	}
	log.Println("Reading entries")
	sb := strings.Builder{}
	sb.WriteString(fmt.Sprintf("// Auto-generated by html/parser/entities_gen(using the JSON file from %s)\n// DO NOT EDIT.\n", *url))
	sb.WriteString(fmt.Sprintf("package %s\n\n", *pkg))
	sb.WriteString("var htmlEntities = map[string]struct{\n    characters string\n    codepoints []int\n}{\n")
	for name, v := range entries {
		sbLine := strings.Builder{}
		characters_, ok := v["characters"]
		if !ok {
			log.Printf("[%s].characters do not exist -- skipping the entry\n", name)
			continue
		}
		characters, ok := characters_.(string)
		if !ok {
			log.Printf("[%s].characters is not string -- skipping the entry\n", name)
			continue
		}

		codepoints_, ok := v["codepoints"]
		if !ok {
			log.Printf("[%s].codepoints do not exist -- skipping the entry\n", name)
			continue
		}
		codepoints, ok := codepoints_.([]any)
		if !ok {
			log.Printf("[%s].codepoints is not array -- skipping the entry\n", name)
			continue
		}

		sbLine.WriteString(fmt.Sprintf("    %s: {characters: %s, codepoints: []int{", strconv.Quote(name), strconv.Quote(characters)))

		first := true
		for i, cp_ := range codepoints {
			cpF, ok := cp_.(float64)
			if !ok {
				log.Printf("[%s].codepoints[%d] is not number -- skipping the codepoint\n", name, i)
				continue
			}
			cpI := int(cpF)
			if !first {
				sbLine.WriteString(", ")
			}
			sbLine.WriteString(fmt.Sprintf("%#x", cpI))
			first = false
		}
		sbLine.WriteString("}},\n")
		sb.WriteString(sbLine.String())
	}
	sb.WriteString("}\n")

	log.Printf("Formatting generated source")
	unformatted := []byte(sb.String())
	formatted, err := format.Source(unformatted)
	if err != nil {
		log.Println(err)
		log.Println("Formatting error -- Writing unformatted source code instead")
		formatted = unformatted
	}
	log.Printf("Writing output to %s", *outFile)
	err = os.WriteFile(*outFile, formatted, 0644)
	if err != nil {
		log.Fatal(err)
	}
}
